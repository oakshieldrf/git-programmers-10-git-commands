class Calculator {
    add(a,b){
        return a+b;
    }

    multiply(a,b) {
        return a*b;
    }

    division(a,b){
        return a/b;
    }

    subtract(a,b){
        return a-b;
    }

    potencia(a,b){
        return Math.pow(a,b);
    }

    modulo(a,b) {
        return a%b;
    }
}